@dataclass(frozen=True)
class HermesValidationResult:
    ok: bool
    endpoint: str
    latency_ms: float
    model: str
    error: str = ""


@dataclass(frozen=True)
class HermesDiagnosticResult:
    ok: bool
    endpoint: str
    host: str
    port: int
    tcp_reachable: bool
    auth_ok: bool
    auth_status_code: int | None
    gateway_state_path: str
    gateway_state: str
    api_server_state: str
    error: str = ""
    recommendation: str = ""


class ReportGenerator(Protocol):
    def generate(self, audit: AuditRecord) -> str:
        """Return a complete self-contained HTML report."""

    def refine_section(self, audit: AuditRecord, current_html: str, section: str, instruction: str) -> str:
        """Return replacement HTML for one report section."""


